import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import matter from "gray-matter";

const webRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const contentRoot = path.join(webRoot, "docs/content");
const outputPath = path.join(webRoot, "src/docs/generated-index.ts");

function listMdx(directory) {
  return fs.readdirSync(directory, { withFileTypes: true }).flatMap((entry) => {
    const target = path.join(directory, entry.name);
    return entry.isDirectory() ? listMdx(target) : entry.name.endsWith(".mdx") ? [target] : [];
  });
}

function headingId(title) {
  return title.toLowerCase().replace(/[`*_]/g, "").replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
}

function headings(source) {
  return [...source.matchAll(/^(#{2,3})\s+(.+)$/gm)].map((match) => ({
    depth: match[1].length,
    id: headingId(match[2]),
    title: match[2].replace(/[`*_]/g, "").trim()
  }));
}

function searchText(source) {
  return source.replace(/<[^>]+>/g, " ").replace(/[`*_#>\[\](){}|]/g, " ").replace(/\s+/g, " ").trim();
}

const index = Object.fromEntries(listMdx(contentRoot).map((file) => {
  const parsed = matter(fs.readFileSync(file, "utf8"));
  return [parsed.data.slug, { headings: headings(parsed.content), searchText: searchText(parsed.content) }];
}).sort(([left], [right]) => left.localeCompare(right)));
const output = `// Generated by scripts/generate-doc-index.mjs. Do not edit by hand.\nimport type { DocHeading } from "./types";\n\nexport const generatedDocsIndex = ${JSON.stringify(index, null, 2)} as Record<string, { headings: DocHeading[]; searchText: string }>;\n`;

if (process.argv.includes("--check")) {
  const current = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, "utf8") : "";
  if (current !== output) {
    console.error("Generated docs index is stale. Run pnpm --filter @robinhood-lb/web docs:generate.");
    process.exitCode = 1;
  } else {
    console.log("Generated docs index is current.");
  }
} else {
  fs.writeFileSync(outputPath, output);
  console.log(`Generated docs index for ${Object.keys(index).length} pages.`);
}
